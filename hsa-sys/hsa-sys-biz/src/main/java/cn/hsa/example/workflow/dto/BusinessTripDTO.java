    private String days;
    /*出差事由*/
    private String cause;
    /*开始时间*/
    private String beginDate;
    /*结束时间*/
    private String endDate;
    /*出差人*/
    private String userId;
    /*姓名*/
    private String userName;
    /*审批人*/
    private String approverId;
    /*审批人*/
    private String approverName;

